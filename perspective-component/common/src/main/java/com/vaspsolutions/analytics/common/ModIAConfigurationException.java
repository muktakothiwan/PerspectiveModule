package com.vaspsolutions.analytics.common;

public class ModIAConfigurationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ModIAConfigurationException() {
		super();
		
	}

	public ModIAConfigurationException(String arg0, Throwable arg1,
			boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public ModIAConfigurationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ModIAConfigurationException(String arg0) {
		super(arg0);
		
	}

	public ModIAConfigurationException(Throwable arg0) {
		super(arg0);
		
	}

}
