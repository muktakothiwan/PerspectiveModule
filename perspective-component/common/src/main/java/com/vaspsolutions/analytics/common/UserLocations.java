package com.vaspsolutions.analytics.common;

import java.io.Serializable;

public class UserLocations implements Serializable{
	private String userName;
	private String city;
	private String state;
	private String country;
	private double latitude;
	private double longitude;
	private String userAuthProfile;
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}
	/**
	 * @return the latitude
	 */
	public double getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	/**
	 * @return the longitude
	 */
	public double getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the userAuthProfile
	 */
	public String getUserAuthProfile() {
		return userAuthProfile;
	}
	/**
	 * @param userAuthProfile the userAuthProfile to set
	 */
	public void setUserAuthProfile(String userAuthProfile) {
		this.userAuthProfile = userAuthProfile;
	}
	
}
